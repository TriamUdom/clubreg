1.นักเรียนชั้นมัธยมศึกษาปีที่ 5 และ 6 ที่ยืนยันการลงทะเบียนชมรมเดิมไปแล้ว ให้ตรวจสอบข้อมูลในระบบว่าถูกต้องหรือไม่ โดยเข้าสู่ระบบและดูชมรมที่นักเรียนยืนยัน
<br>
2.นักเรียนชั้นมัธยมศึกษาปีที่ 4, 5 และ 6 ทุกคนที่ประสงค์จะลงทะเบียนชมรมที่มีการคัดเลือก (ออดิชัน)
ให้เลือกชมรมที่นักเรียนต้องการสมัคร (เลือกได้มากกว่า 1 ชมรม)
<br>
ได้ตั้งแต่วันที่ 16 - 19 พฤษภาคม 2559
และเข้ารับการคัดเลือกตามที่แต่ละชมรมกำหนดภายในวันที่ 19 พฤษภาคม 2559
<br>
3.เมื่อนักเรียนผ่านการคัดเลือกแล้ว ระบบจะแสดงรายชื่อชมรมที่นักเรียนผ่านการทดสอบ
<b style="color:red;">นักเรียนสามารถเลือกได้เพียง 1 ชมรม โดยกดปุ่ม "ยืนยัน" และเมื่อเลือกแล้ว จะไม่สามารถเปลี่ยนแปลงได้อีก</b>
โดยนักเรียนต้องกดปุ่ม "ยืนยัน" ภายใน เวลา 12.00 น. ของวันที่ 19 พฤษภาคม 2559 มิเช่นนั้น ถือว่านักเรียนสละสิทธิ์
<br>
4.เมื่อนักเรียนเลือกชมรมแล้วระบบจะแสดงปีการศึกษา และชื่อชมรมที่นักเรียนเลือก
<br>
5.หากนักเรียนต้องการสมัครชมรมที่ไม่มีการคัดเลือก (ออดิชัน) หรือไม่ผ่านการคัดเลือก นักเรียนจะสามารถลงทะเบียนได้เลยในวันที่ 20 พฤษภาคม 2559
เวลา 09.00น.
